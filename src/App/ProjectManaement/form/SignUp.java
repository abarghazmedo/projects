    public boolean dublicate() {
        String email = txt_email.getText();
        boolean isExest = false;
        try {
            Connection con = DBconnection.getConnection();

            PreparedStatement prs = con.prepareStatement("select * from employer email=? ");
            prs.setString(2, email);
            ResultSet rs = prs.executeQuery();
            if (rs.next()) {
                isExest = true;
            } else {
                isExest = false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return isExest;
    }

