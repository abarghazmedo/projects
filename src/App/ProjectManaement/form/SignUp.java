    //method to insert values intot employer table
    public void insertSignupDetail() {
        String name = txt_username.getText();
        String email = txt_email.getText();
        String password = txt_password.getText();
        try {
            Connection con = DBconnection.getConnection();
            String sql = "insert into employer (Name,Email,Password)value(?,?,?)";
            PreparedStatement prs = con.prepareStatement(sql);

            prs.setString(1, name);
            prs.setString(2, email);
            prs.setString(3, password);

            int updatedrowcont = prs.executeUpdate();
            if (updatedrowcont > 0) {
                JOptionPane.showMessageDialog(this, "recorded Inserted Successfully");
            } else {
                JOptionPane.showMessageDialog(this, "recording Insertion failed");
            }

        } catch (Exception e) {

            e.printStackTrace();

        }
    }

    //method to login employer table
    public boolean validateForm() {
        String name = txt_username.getText();
        String email = txt_email.getText();
        String password = txt_password.getText();
        if (name.equals("")) {
            JOptionPane.showMessageDialog(this, "please entre User Name ");
            return false;
        }
        if (email.equals("") || email.matches("^(.+)@(.+)$")) {
            JOptionPane.showMessageDialog(this, "please entre Email ");
            return false;
        }
        if (password.equals("")) {
            JOptionPane.showMessageDialog(this, "please entre Password ");
            return false;
        }
        return true;
    }
